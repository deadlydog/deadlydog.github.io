<h1>Torrent Information</h1>

<h3>So What Are Torrents</h3>
Torrents are basically just little files which contain information about other people who are hosting (seeding) files.
So basically, you just download a torrent file, open it using a torrent program (such as BitTornado or Azureus), which will then
connect to the other people specified in the torrent file and start downloading the files from the other people. Once you open up a 
torrent file, you are then also hosting that file, so as you download the files from other people, other people can download the 
files from you.

<h3>What Client Should I Use</h3>
I like uTorrent (<a href="http://www.utorrent.com/" target="_blank">http://www.bittornado.com/</a>), but a lot of people like 
Azureus (<a href="http://azureus.sourceforge.net/" target="_blank">http://azureus.sourceforge.net/</a>). You can try each of them 
out and see which one you like better. There are many other clients, but uTorrent is my favorite.

<h3>Where Can I Get Torrents</h3>
